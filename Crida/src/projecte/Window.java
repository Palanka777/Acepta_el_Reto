    private void botoInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botoInsertarActionPerformed
        // TODO add your handling code here:
        Treballador nou=new Treballador();
        try{
            
            nou.set1_nom(nomText.getText().strip());
            nou.set2_cognom(cognomText.getText().strip());
            nou.set3_telefon(Integer.parseInt(telefonText.getText().strip()));
            nou.set4_categoria(categoriaText.getText().strip());
            nou.set5_hores(Integer.parseInt(horesText.getText().strip()));
        
        int i;
            for (i = 0; i < p1.length && p1[i]!=null; i++);
            if(i<p1.length){
                p1[i]=nou;
                refresca();
                if(++plenes==p1.length)botoInsertar.setEnabled(false);
                if(i>0)botoBorrar.setEnabled(true);
                
            }
        }catch(java.lang.NumberFormatException ex){
            JOptionPane.showMessageDialog(this,"Tens dades incorrectes!");
        
        }catch(java.lang.NullPointerException ex){
            JOptionPane.showMessageDialog(this,"No pots tenir un numero negatiu!");
        }
    }//GEN-LAST:event_botoInsertarActionPerformed

    private void botoBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botoBorrarActionPerformed
        // TODO add your handling code here:
        int filaSel=taula.getSelectedRow();
        
        if(filaSel!=1){
            taula.getColumnModel().addColumn(tc);
            
            Treballador borrar=(Treballador)taula.getModel().getValueAt(filaSel, taula.getColumnCount()-1);
            taula.getColumnModel().removeColumn(tc);
            
            int i;
            for (i = 0; i < p1.length && (p1[i]==null) || !p1[i].equals(borrar); i++);
                if(i<p1.length)
                {
                 
                    p1[i]=null;
                    refresca();
                    JOptionPane.showMessageDialog(this, "El treballador\n\n"+borrar.get1_nom()+" "+borrar.get2_cognom()+"\n\n"+"Ha sigut borrat correctament\n");
                    
                }else JOptionPane.showMessageDialog(this, "Per borrar has de seleccionar una fila");
            
                p1[filaSel]=null;
                refresca();
                botoInsertar.setEnabled(true);
                
                if(--plenes==0)botoBorrar.setEnabled(false);
                 
            }else JOptionPane.showMessageDialog(this, "Per borrar has de seleccionar una fila");
  
         
    }//GEN-LAST:event_botoBorrarActionPerformed

