    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        // TODO add your handling code here:
        ObjectOutputStream sortida=null;
        
        try {
            // TODO add your handling code here:
            sortida=new ObjectOutputStream(new BufferedOutputStream(new FileOutputStream(Projecte.fitxerDades)));
        
            for (int i = 0; i < p1.length; i++) {                
                if(p1[i]!=null)sortida.writeObject(p1[i]);
            }
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(this,"Atencio possible perdua de dades");
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this,"Atencio possible perdua de dades");
        }finally{
            
            try {
                if(sortida!=null)sortida.close();
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this,"Atencio possible perdua de dades");
            }
        }
    }//GEN-LAST:event_formWindowClosing

