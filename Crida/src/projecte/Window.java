    private void llegirFitxerDades(){
        
       if (Projecte.fitxerDades.exists()){
           
           ObjectInputStream entrada=null;
           int i=0;
           try {
               entrada=new ObjectInputStream(new BufferedInputStream(new FileInputStream(Projecte.fitxerDades)));
           
               for(i=0; i<p1.length; i++){
                   p1[i]=(Treballador)entrada.readObject();
                  
               }
               if(i==p1.length){
                   entrada.readObject();
                   JOptionPane.showMessageDialog(this, "Atencio, no tens mes espai a la memoria del fitxer, pots ampliar-la modifican el fitxer config.txt");
                   System.exit(0);
               }
             
               
           } catch (FileNotFoundException ex) {
               JOptionPane.showMessageDialog(this,"Atencio possible perdua de dades");
           } catch (IOException ex) {
                   //si entrem aqui es que ha anat tot be
                   
                plenes=i;
               if(plenes==p1.length)botoInsertar.setEnabled(false);
           } catch (ClassNotFoundException ex) {
               JOptionPane.showMessageDialog(this,"Atencio possible perdua de dades");
           }finally{
               
               if(entrada!=null)
                   try{
                       entrada.close();
                   }catch (IOException ex){
                       JOptionPane.showMessageDialog(this,"Atencio possible perdua de dades");
                   }
           
           }
           
       }
       
    }

    private void llegirFitxerConfig() {
        
        BufferedReader configIn=null;
        PrintWriter configOut=null;
        
        try{
            if(Projecte.fitxerConfig.exists()){
               
                configIn=new BufferedReader(new FileReader(Projecte.fitxerConfig));
                p1=new Treballador[Integer.parseInt(configIn.readLine().strip())];

            }else{
                
                configOut=new PrintWriter(Projecte.fitxerConfig);
                configOut.print(50);
                configOut.print("12345");
                throw new NumberFormatException();
            }
            
        } catch (NumberFormatException | IOException ex) {
            JOptionPane.showMessageDialog(this,"Atencio possible perdua de dades");
            p1=new Treballador[8];
        }finally{
            
            try{
                if(configIn!=null)configIn.close();
                if(configOut!=null)configOut.close();
                
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this,"Atencio possible perdua de dades");
            }
        }
        
    }

